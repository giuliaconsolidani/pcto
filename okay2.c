#include <stdio.h>

int main()
{   
   int  n;
   int y;
   int x;

   n = 3;
   y = 2;
   x = n+y;
   printf("n: %d y: %d x = %d\n", n, y, x);

   int a;
   printf("a: %d", a);

   int  b;
   int  c;
   int  d;

   b = 3;
   c = 2;
   d = b-c;
   printf("b: %d c: %d d = %d\n", b, c, d);

  
   int  e;
   int  f;
   int  g;

   e = 3;
   f = 2;
   g = e*f;
   printf("e: %d f: %d g = %d\n", e, f, g); 

   int  h;
   int  i;
   float l;

   h = 3;
   i = 2;
   l = ((float)h)/i;
   printf("h: %d i: %d l = %f\n", h, i, l); 
}